#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t	i;
	char	*cdest;
	char	*csrc;

	i = 0;
	cdest = (char *)dest;
	csrc = (char *)src;
	while (i < n)
	{
		if (cdest == csrc)
			return (0);
		cdest[i] = csrc[i];
		i++;
	}
	return (dest);
}
